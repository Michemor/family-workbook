  /// Returns the CountryData matching the given ISO code (default: US)
  static CountryData fromCode(String code) {
    return all.firstWhere(
      (c) => c.code == code,
      orElse: () => all.firstWhere((c) => c.code == 'US'),
    );
  }

  /// Returns the list of country names only (for country dropdowns)
  static List<String> get countryNames => all.map((c) => '${c.flag}  ${c.name}').toList();

  /// Returns plain name given a display label
  static String plainName(String displayName) {
    // Strip the emoji prefix
    final parts = displayName.split('  ');
    return parts.length > 1 ? parts.last : displayName;
  }
